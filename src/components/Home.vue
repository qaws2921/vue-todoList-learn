<template>
  <v-container>
    <v-layout row wrap>
      <v-flex xs6 pa-2>
        <List 
          :todoList = "todoList"
          @statusControl="statusControl"
          @listDelete="listDelete"
        ></List>
      </v-flex>
      <v-flex xs6 pa-2>
        <ListAdd @listAdd='listAdd'></ListAdd>
      </v-flex>
    </v-layout>
  </v-container>
</template>

<script>
  import List from './List'
  import ListAdd from './ListAdd'

  export default {
    components: {
      List,
      ListAdd
    },
    data() {
      return {
        todoList: []
      }
    },
    methods: {
      listAdd(memo) {
        console.log("방았어!")
        
        this.todoList.push({
          memo: memo,
          status: "created"
          }
        )
      },
      statusControl(index, status){
        this.todoList[index].status = status
      },
      listDelete(index) {
        this.todoList.splice(index,1)
      }
    }
  }
</script>